using UnityEngine;

// Generates a simple quad mesh procedurally

public class Mesh_Test : MonoBehaviour
{
    public float width = 1;
    public float height = 1;
    public Material myDefaultMaterial;

    public void Start()
    {
        MeshRenderer meshRenderer = gameObject.AddComponent<MeshRenderer>();
        meshRenderer.sharedMaterial = myDefaultMaterial;

        MeshFilter meshFilter = gameObject.AddComponent<MeshFilter>();

        // Our mesh to work with
        Mesh mesh = new Mesh();

        // Vertices are basically corners of a triangle in 3D space
        Vector3[] vertices = new Vector3[4]
        {
            new Vector3(0, 0, 0),
            new Vector3(width, 0, 0),
            new Vector3(0, height, 0),
            new Vector3(width, height, 0)
        };
        mesh.vertices = vertices;

        // Tris explain the corners of each triangle as index of vertices array
        int[] tris = new int[6]
        {
            // lower left triangle
            0, 2, 1,
            // upper right triangle
            2, 3, 1
        };
        mesh.triangles = tris;

        // The light does not work when it hits to the normal from behind
        Vector3[] normals = new Vector3[4]
        {
            -Vector3.forward,
            -Vector3.forward,
            -Vector3.forward,
            -Vector3.forward
        };
        mesh.normals = normals;

        // The uv 
        Vector2[] uv = new Vector2[4]
        {
            new Vector2(0, 0),
            new Vector2(1, 0),
            new Vector2(0, 1),
            new Vector2(1, 1)
        };
        mesh.uv = uv;

        meshFilter.mesh = mesh;
    }
}